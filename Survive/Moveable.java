/**
 * [Moveable.java]
 * A interface that allows things to move around in the four overall directions(up, down, left, right).
 * @author Edward Wu
 * @version 1.0, June 15, 2021
 */
interface Moveable {
    public void moveUp();
    public void moveDown();
    public void moveLeft();
    public void moveRight();
}
